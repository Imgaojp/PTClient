@Mapper
@Component
public interface RecentInfoMapper {

    @Insert("insert into log(site,upload,download,shareRatio,bonus,uploadCount,uploadTime,date) values( #{site},#{upload},#{download},#{shareRatio},#{bonus},#{uploadCount},#{uploadTime},#{date})")
    int insert(RecentInfo recentInfo);

    @Select("select * from log order by date")
    List<RecentInfo> all();

    @Delete("delete from log where site=#{site}")
    void remove(String site);

    @Select("select * from log where site=#{site} order by date ")
    List<RecentInfo> getSiteRecentInfo(String site);

    @Select("select * from log where site=#{site} order by date limit 30")
    List<RecentInfo> get30RecentInfo(String site);
}
