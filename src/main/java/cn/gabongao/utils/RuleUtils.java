@Component
public class RuleUtils {
    @Value("${host}")
    String host;

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }
    @Autowired
    private HttpClientDownloader downloader;

    public void all() {
        Page download = downloader.download(new Request(host + "rule.json"));
        Json json = download.getJson();
        List<Rule> list = json.toList(Rule.class);
        for (Rule rule :
                list) {
            Context.ruleMap.put(rule.getSite(), rule);
        }
    }

}
