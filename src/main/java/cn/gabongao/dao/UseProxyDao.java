@Component
public class UseProxyDao {


    private final UseProxyListMapper dao;

    @Autowired
    public UseProxyDao(UseProxyListMapper dao) throws MalformedURLException {
        this.dao = dao;

        for (String str :
                dao.all()) {
            URL url = new URL(str);
            Context.useProxy.add(url.getHost());
        }

        Context.useProxy.addAll(dao.all());
    }

    public List<String> all() {
        return dao.all();
    }

}
